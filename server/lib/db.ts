// ── CC review reports (claude CLI output) ───────────────────────────────────

export interface CcReview {
  content: string;
  created_at: string;
}

export function setCcReview(
  scope: string,
  sessionId: string | null,
  content: string
): void {
  const db = getDb();
  db.prepare(
    "DELETE FROM cc_reviews WHERE scope = ? AND IFNULL(session_id,'') = IFNULL(?,'')"
  ).run(scope, sessionId);
  db.prepare(
    "INSERT INTO cc_reviews (scope, session_id, content, created_at) VALUES (?, ?, ?, ?)"
  ).run(scope, sessionId, content, new Date().toISOString());
}

export function getCcReview(
  scope: string,
  sessionId: string | null
): CcReview | null {
  const db = getDb();
  const row = db
    .prepare(
      `SELECT content, created_at FROM cc_reviews
       WHERE scope = ? AND IFNULL(session_id,'') = IFNULL(?,'')
       ORDER BY created_at DESC LIMIT 1`
    )
    .get(scope, sessionId) as CcReview | undefined;
  return row ?? null;
}
