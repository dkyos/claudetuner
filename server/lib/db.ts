// SQLite storage backed by Node's builtin `node:sqlite` (synchronous, no native
// install). One DB file lives at server/data.sqlite. The connection is cached on
// globalThis so Next.js dev HMR doesn't reopen it on every module re-eval.
import { DatabaseSync } from "node:sqlite";
import path from "node:path";

const DB_PATH = path.join(process.cwd(), "data.sqlite");

type GlobalWithDb = typeof globalThis & { __ctDb?: DatabaseSync };

function init(): DatabaseSync {
  const db = new DatabaseSync(DB_PATH);
  db.exec("PRAGMA journal_mode = WAL;");
  db.exec(`
    CREATE TABLE IF NOT EXISTS snapshots (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      user_email TEXT NOT NULL,
      provider TEXT,
      plan TEXT,
      org_uuid TEXT,
      install_id TEXT,
      five_hour_utilization REAL,
      five_hour_resets_at TEXT,
      seven_day_utilization REAL,
      seven_day_resets_at TEXT,
      extra_usage_used REAL,
      extra_usage_limit REAL,
      collected_at TEXT,
      raw TEXT,
      created_at TEXT NOT NULL
    );
  `);

  // Migration: older DBs predate the provider column. Add it and backfill from
  // raw so existing rows (Claude/ChatGPT/Gemini mixed under one email) split.
  const cols = db
    .prepare("SELECT name FROM pragma_table_info('snapshots')")
    .all() as unknown as { name: string }[];
  if (!cols.some((c) => c.name === "provider")) {
    db.exec("ALTER TABLE snapshots ADD COLUMN provider TEXT");
  }
  const needBackfill = db
    .prepare("SELECT id, raw FROM snapshots WHERE provider IS NULL")
    .all() as unknown as { id: number; raw: string }[];
  if (needBackfill.length) {
    const upd = db.prepare("UPDATE snapshots SET provider = ? WHERE id = ?");
    for (const r of needBackfill) {
      let p = "claude";
      try {
        p = JSON.parse(r.raw).provider || "claude";
      } catch {
        /* keep default */
      }
      upd.run(p, r.id);
    }
  }

  db.exec(
    "CREATE INDEX IF NOT EXISTS idx_snapshots_email_provider_collected ON snapshots(user_email, provider, collected_at);"
  );

  // Claude Code (CLI) transcripts scanned from ~/.claude/projects.
  // Migration: drop pre-user-metrics cc tables so they rebuild with the new
  // schema (a full rescan repopulates everything from the local transcripts).
  const ccCols = db
    .prepare("SELECT name FROM pragma_table_info('cc_sessions')")
    .all() as unknown as { name: string }[];
  if (ccCols.length && !ccCols.some((c) => c.name === "user_turns")) {
    db.exec("DROP TABLE IF EXISTS cc_messages");
    db.exec("DROP TABLE IF EXISTS cc_sessions");
  }
  db.exec(`
    CREATE TABLE IF NOT EXISTS cc_sessions (
      session_id TEXT PRIMARY KEY,
      project TEXT,
      cwd TEXT,
      title TEXT,
      git_branch TEXT,
      cc_version TEXT,
      started_at TEXT,
      ended_at TEXT,
      message_count INTEGER DEFAULT 0,
      input_tokens INTEGER DEFAULT 0,
      output_tokens INTEGER DEFAULT 0,
      cache_tokens INTEGER DEFAULT 0,
      user_turns INTEGER DEFAULT 0,
      user_chars INTEGER DEFAULT 0,
      first_user_prompt TEXT,
      source_path TEXT,
      mtime_ms INTEGER,
      synced_at TEXT
    );
  `);
  db.exec(`
    CREATE TABLE IF NOT EXISTS cc_messages (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      session_id TEXT NOT NULL,
      idx INTEGER,
      role TEXT,
      kind TEXT,
      text TEXT,
      tool_name TEXT,
      created_at TEXT
    );
  `);
  db.exec(
    "CREATE INDEX IF NOT EXISTS idx_cc_sessions_started ON cc_sessions(started_at);"
  );
  db.exec(
    "CREATE INDEX IF NOT EXISTS idx_cc_messages_session ON cc_messages(session_id, idx);"
  );

  // LLM review reports generated by calling the `claude` CLI (scope: overall|session).
  db.exec(`
    CREATE TABLE IF NOT EXISTS cc_reviews (
      id INTEGER PRIMARY KEY AUTOINCREMENT,
      scope TEXT NOT NULL,
      session_id TEXT,
      content TEXT,
      created_at TEXT
    );
  `);
  return db;
}

export function getDb(): DatabaseSync {
  const g = globalThis as GlobalWithDb;
  if (!g.__ctDb) g.__ctDb = init();
  return g.__ctDb;
}

