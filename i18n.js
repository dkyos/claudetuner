let _currentLang = 'en';

function detectLang() {
  const browserLang = (navigator.language || 'en').slice(0, 2).toLowerCase();
  return browserLang === 'ko' ? 'ko' : 'en';
}

async function initI18n() {
  const { lang } = await chrome.storage.sync.get({ lang: 'auto' });
  _currentLang = lang === 'auto' ? detectLang() : lang;
  document.documentElement.lang = _currentLang;
  applyI18n();
}

function setLang(lang) {
  _currentLang = lang === 'auto' ? detectLang() : lang;
  document.documentElement.lang = _currentLang;
  applyI18n();
}

function t(key, ...args) {
  const dict = TRANSLATIONS[_currentLang] || TRANSLATIONS.en;
  let text = dict[key] || TRANSLATIONS.en[key] || key;
  args.forEach((a, i) => { text = text.replaceAll(`{${i}}`, a); });
  return text;
}

function getLang() {
  return _currentLang;
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n');
    el.textContent = t(key);
  });
  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    const key = el.getAttribute('data-i18n-html');
    el.innerHTML = t(key);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
  });
  document.querySelectorAll('[data-i18n-title]').forEach((el) => {
    el.title = t(el.getAttribute('data-i18n-title'));
  });
  document.querySelectorAll('a[href*="claudetuner.com/welcome"]').forEach((el) => {
    const u = new URL(el.href);
    u.searchParams.set('lang', _currentLang);
    el.href = u.toString();
  });
}
